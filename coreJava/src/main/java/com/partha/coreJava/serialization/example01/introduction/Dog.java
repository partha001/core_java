package com.partha.coreJava.serialization.example01.introduction;

import java.io.Serializable;

public class Dog implements Serializable{
	
	private int id;
	private String name;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
